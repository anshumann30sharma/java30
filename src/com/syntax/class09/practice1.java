package com.syntax.class09;

public class practice1 {

	public static void main(String[] args) {
		/* Write a program to print out the pattern:  
$$$$
$  $
$  $
$$$$
*/
		for(int i=3;i<4;i++) {
			for(int j=1;j<5;j++) {
				System.out.print("$");
			}
			System.out.println();	
	}
		for(int i=3;i<4;i++) {
			for(int j=4;j<5;j++) {
				System.out.print("$"+"  "+"$");
			}
			System.out.println();	
}
		for(int i=3;i<4;i++) {
			for(int j=4;j<5;j++) {
				System.out.print("$"+"  "+"$");
			}
			System.out.println();
}
		for(int i=3;i<4;i++) {
			for(int j=1;j<5;j++) {
				System.out.print("$");
			}
			System.out.println();
}
}
}
